/**
 * Holds a weather element's / sensor's high and low of the current year
 */
export default class YearlyHighAndLow {
    /**
     * @hidden
     */
    constructor() {}
    /**
     * The low of the current year
     */
    public low: number | null = null;
    /**
     * The high of the current year
     */
    public high: number | null = null;
}
